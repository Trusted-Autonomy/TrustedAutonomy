    /// Recursively collect every `.rs` file under `apps/` and `crates/`,
    /// skipping `target/` build output.
    fn collect_rs_files(root: &std::path::Path, out: &mut Vec<std::path::PathBuf>) {
        let Ok(entries) = std::fs::read_dir(root) else {
            return;
        };
        for entry in entries.flatten() {
            let path = entry.path();
            if path.is_dir() {
                let name = path.file_name().and_then(|n| n.to_str()).unwrap_or("");
                if name == "target" || name.starts_with('.') {
                    continue;
                }
                collect_rs_files(&path, out);
            } else if path.extension().and_then(|e| e.to_str()) == Some("rs") {
                out.push(path);
            }
        }
    }

    /// Extract the literal argument of every `.join("<literal>")` call that
    /// directly follows a `.join(".ta")` call in `content` — i.e. `X.join(".ta").join("foo")`
    /// yields `"foo"`. This is a deliberately narrow heuristic (source text
    /// scanning, not real parsing): it only catches the direct-chain form,
    /// not `let ta_dir = x.join(".ta"); ta_dir.join("foo")`. It's a safety
    /// net on top of code review, not a substitute for it.
    fn extract_dot_ta_join_literals(content: &str) -> Vec<String> {
        const MARKER: &str = ".join(\".ta\")";
        let mut results = Vec::new();
        let mut search_from = 0;
        while let Some(pos) = content[search_from..].find(MARKER) {
            let abs = search_from + pos + MARKER.len();
            let rest = content[abs..].trim_start();
            if let Some(after_join) = rest.strip_prefix(".join(\"") {
                if let Some(end) = after_join.find('"') {
                    results.push(after_join[..end].to_string());
                }
            }
            search_from = abs;
        }
        results
    }

