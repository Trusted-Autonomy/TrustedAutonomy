/// Returns only the directory entries from `LOCAL_TA_PATHS` (those ending with `/`).
///
/// Used by IDE integrations (VS Code `files.exclude`, JetBrains `DirectoryIndexExcludePolicy`)
/// to exclude TA runtime directories from file search and symbol indexing. File entries and
/// glob patterns are omitted — IDEs handle those differently.
pub fn ta_runtime_dirs() -> impl Iterator<Item = &'static str> {
    LOCAL_TA_PATHS.iter().copied().filter(|p| p.ends_with('/'))
}

/// VCS backend detected or configured for the project.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum VcsBackend {
    Git,
    Perforce,
    None,
}

impl VcsBackend {
    /// Detect the VCS backend for the given project root.
    ///
    /// Check order:
    /// 1. `.git/` directory present — Git
    /// 2. `.p4config` in any parent directory or `P4PORT`/`P4CLIENT` env vars — Perforce
    /// 3. Otherwise — None
    pub fn detect(project_root: &std::path::Path) -> Self {
        // Git: .git/ directory exists, or git rev-parse succeeds.
        if project_root.join(".git").exists() {
            return Self::Git;
        }
        // Try git rev-parse (handles worktrees and submodules).
        // Clear TA agent VCS isolation env vars (set by v0.13.17.3) so we
        // detect VCS based on project_root's own repo, not the staging dir.
        let git_ok = std::process::Command::new("git")
            .args(["rev-parse", "--git-dir"])
            .current_dir(project_root)
            .env_remove("GIT_DIR")
            .env_remove("GIT_WORK_TREE")
            .env_remove("GIT_CEILING_DIRECTORIES")
            .output()
            .map(|o| o.status.success())
            .unwrap_or(false);
        if git_ok {
            return Self::Git;
        }

        // Perforce: P4PORT or P4CLIENT env vars, or .p4config in ancestor dirs.
        if std::env::var("P4PORT").is_ok() || std::env::var("P4CLIENT").is_ok() {
            return Self::Perforce;
        }
        let mut dir = Some(project_root);
        while let Some(d) = dir {
            if d.join(".p4config").exists() {
                return Self::Perforce;
            }
            dir = d.parent();
        }

        Self::None
    }

    /// Machine-readable name for workflow.toml serialization.
    pub fn as_str(&self) -> &'static str {
        match self {
            Self::Git => "git",
            Self::Perforce => "perforce",
            Self::None => "none",
        }
    }

    /// Check whether a `.ta/`-relative path is ignored by this project's VCS.
    ///
    /// Returns `Ok(true)` if the VCS is configured to ignore the path,
    /// `Ok(false)` if it is not (warrants a `ta setup vcs` nudge),
    /// or `Err(String)` if the VCS check itself failed.
    ///
    /// Dispatches to the per-VCS free function so callers (`ta doctor`,
    /// `ta plan shared`, `ta goal`) do not need VCS-specific branches.
    ///
    /// `VcsBackend::None` always returns `Ok(true)` — no VCS means nothing
    /// will ever be committed, so all paths are effectively "safe".
    pub fn is_path_ignored(
        &self,
        project_root: &std::path::Path,
        ta_rel_path: &str,
    ) -> Result<bool, String> {
        match self {
            Self::Git => git_is_ignored(project_root, ta_rel_path),
            Self::Perforce => p4_is_ignored(project_root, ta_rel_path),
            Self::None => Ok(true),
        }
    }
}

/// Check whether a given `.ta/` relative path is currently ignored by Git.
///
/// Returns `Ok(true)` if ignored, `Ok(false)` if tracked/untracked-but-not-ignored.
/// Returns `Err` if the check itself failed (not a git repo, git not on PATH, etc.).
pub fn git_is_ignored(project_root: &std::path::Path, ta_rel_path: &str) -> Result<bool, String> {
    let path = format!(".ta/{}", ta_rel_path);
    let output = std::process::Command::new("git")
        .args(["check-ignore", "-q", &path])
        .current_dir(project_root)
        .output()
        .map_err(|e| format!("git check-ignore failed: {}", e))?;
    // exit 0 = ignored, exit 1 = not ignored, other = error
    if output.status.success() {
        Ok(true)
    } else if output.status.code() == Some(1) {
        Ok(false)
    } else {
        Err(format!(
            "git check-ignore returned unexpected status: {:?}",
            output.status.code()
        ))
    }
}

/// Check whether a `.ta/`-relative path is excluded by Perforce's P4IGNORE rules.
///
/// Returns `Ok(true)` if the path is excluded, `Ok(false)` if it is not.
/// Returns `Err` if the command itself failed.
///
/// Requires `P4IGNORE` to be set in the environment and `p4` on `$PATH`.
/// When `P4IGNORE` is not set, Perforce has no ignore rules in effect, so
/// the path is not protected — returns `Ok(false)`.
///
/// Uses `p4 ignores -i <path>` (Helix Core 2018.2+). Output contains
/// "is excluded by" when the path is excluded.
pub fn p4_is_ignored(project_root: &std::path::Path, ta_rel_path: &str) -> Result<bool, String> {
    // Without P4IGNORE, no ignore rules are active.
    if std::env::var("P4IGNORE").is_err() {
        return Ok(false);
    }
    let path = format!(".ta/{}", ta_rel_path);
    let output = std::process::Command::new("p4")
        .args(["ignores", "-i", &path])
        .current_dir(project_root)
        .output()
        .map_err(|e| format!("p4 ignores failed: {}", e))?;
    // "p4 ignores -i <path>" prints "<path> is excluded by: ..." when ignored,
    // or "<path> is not excluded" when not.
    let stdout = String::from_utf8_lossy(&output.stdout);
    Ok(stdout.contains("is excluded by"))
}

/// The marker line used in .gitignore to identify the TA-managed block.
pub const GITIGNORE_MARKER: &str = "# Trusted Autonomy — local runtime state (do not commit)";

/// Build the `.gitignore` block for all local TA paths.
pub fn gitignore_block() -> String {
    let mut block = String::new();
    block.push_str(GITIGNORE_MARKER);
    block.push('\n');
    for path in LOCAL_TA_PATHS {
        block.push_str(&format!(".ta/{}\n", path));
    }
    block
}

/// Whether `.ta/<rest>` (the full line, e.g. `.ta/staging/`) is a recognized
/// entry — i.e. it matches something in `LOCAL_TA_PATHS` or `SHARED_TA_PATHS`,
/// including the `*.log` / `*.pid` / `*.lock` glob entries.
///
/// Used by [`update_gitignore`] / [`update_p4ignore`] to distinguish TA's own
/// generated lines (safe to regenerate) from entries a human added by hand
/// that TA doesn't know about (must be preserved, not silently dropped —
/// see v0.17.0.12.32).
fn is_recognized_ta_line(line: &str) -> bool {
    let Some(rel) = line.strip_prefix(".ta/") else {
        return false;
    };
    LOCAL_TA_PATHS
        .iter()
        .chain(SHARED_TA_PATHS.iter())
        .any(|p| {
            if let Some(suffix) = p.strip_prefix("*.") {
                rel.ends_with(suffix) && !rel.contains('/')
            } else {
                *p == rel
            }
        })
}

/// Append (or update) the TA block in the given `.gitignore` content.
///
/// Idempotent: if the marker is already present, replaces the existing block.
/// If `force` is true, always rewrites the block even if already present.
///
/// Returns `(new_content, changed, unregistered_lines)`. `unregistered_lines`
/// lists any `.ta/`-prefixed line found in the old TA block that isn't a
/// recognized `LOCAL_TA_PATHS`/`SHARED_TA_PATHS` entry — these are preserved
/// in the output (never silently dropped), but callers should surface them
/// as a warning so the entry can be registered properly (v0.17.0.12.32).
pub fn update_gitignore(existing: &str, force: bool) -> (String, bool, Vec<String>) {
    if !force && existing.contains(GITIGNORE_MARKER) {
        // Already present — nothing to do.
        return (existing.to_string(), false, Vec::new());
    }

    // Remove any existing TA block (marker line + the .ta/ lines that follow),
    // but only the lines we actually recognize — anything else that happens
    // to start with `.ta/` is preserved and reported, not dropped.
    let mut result = String::new();
    let mut in_ta_block = false;
    let mut unregistered = Vec::new();
    for line in existing.lines() {
        if line.trim() == GITIGNORE_MARKER.trim() {
            in_ta_block = true;
            continue;
        }
        if in_ta_block {
            let trimmed = line.trim();
            if trimmed.starts_with(".ta/") {
                if is_recognized_ta_line(trimmed) {
                    continue;
                }
                unregistered.push(trimmed.to_string());
                result.push_str(line);
                result.push('\n');
                continue;
            }
            in_ta_block = false;
        }
        result.push_str(line);
        result.push('\n');
    }

    // Ensure there's exactly one blank line separator before our block.
    if !result.is_empty() && !result.ends_with("\n\n") {
        if result.ends_with('\n') {
            result.push('\n');
        } else {
            result.push_str("\n\n");
        }
    }

    result.push_str(&gitignore_block());
    (result, true, unregistered)
}

/// The marker line used in .p4ignore to identify the TA-managed block.
pub const P4IGNORE_MARKER: &str = "# Trusted Autonomy — local runtime state (do not submit)";

/// Build the `.p4ignore` block for all local TA paths.
pub fn p4ignore_block() -> String {
    let mut block = String::new();
    block.push_str(P4IGNORE_MARKER);
    block.push('\n');
    for path in LOCAL_TA_PATHS {
        block.push_str(&format!(".ta/{}\n", path));
    }
    block
}

/// Append (or update) the TA block in the given `.p4ignore` content.
///
/// Idempotent: if the marker is already present, skips unless `force`.
///
/// Returns `(new_content, changed, unregistered_lines)` — see
/// [`update_gitignore`] for the meaning of `unregistered_lines`.
pub fn update_p4ignore(existing: &str, force: bool) -> (String, bool, Vec<String>) {
    if !force && existing.contains(P4IGNORE_MARKER) {
        return (existing.to_string(), false, Vec::new());
    }

    let mut result = String::new();
    let mut in_ta_block = false;
    let mut unregistered = Vec::new();
    for line in existing.lines() {
        if line.trim() == P4IGNORE_MARKER.trim() {
            in_ta_block = true;
            continue;
        }
        if in_ta_block {
            let trimmed = line.trim();
            if trimmed.starts_with(".ta/") {
                if is_recognized_ta_line(trimmed) {
                    continue;
                }
                unregistered.push(trimmed.to_string());
                result.push_str(line);
                result.push('\n');
                continue;
            }
            in_ta_block = false;
        }
        result.push_str(line);
        result.push('\n');
    }

    if !result.is_empty() && !result.ends_with("\n\n") {
        if result.ends_with('\n') {
            result.push('\n');
        } else {
            result.push_str("\n\n");
        }
    }
    result.push_str(&p4ignore_block());
    (result, true, unregistered)
}

