    fn is_known_or_allowlisted(bare: &str) -> bool {
        let known = LOCAL_TA_PATHS
            .iter()
            .chain(SHARED_TA_PATHS.iter())
            .any(|p| {
                let p_bare = p.trim_end_matches('/');
                if let Some(suffix) = p.strip_prefix("*.") {
                    bare.ends_with(suffix) && !bare.contains('/')
                } else {
                    p_bare == bare
                }
            });
        known || KNOWN_UNTRIAGED_GAPS.contains(&bare)
    }

    /// Safety net for v0.17.0.12.32: fail CI if a *new* `.ta/`-relative
    /// literal path shows up in the source that isn't registered in
    /// `LOCAL_TA_PATHS`/`SHARED_TA_PATHS` (or the known pre-existing
    /// backlog above). This is what should have caught the missing
    /// `human-verify-audit.jsonl` et al. before they ever reached
    /// `.gitignore` by hand.
    #[test]
    fn all_direct_dot_ta_join_literals_are_registered_or_allowlisted() {
        let manifest_dir = std::path::Path::new(env!("CARGO_MANIFEST_DIR"));
        let workspace_root = manifest_dir
            .parent()
            .and_then(|p| p.parent())
            .expect("crates/ta-workspace should be two levels below the workspace root");

        let mut files = Vec::new();
        for top in ["apps", "crates"] {
            let dir = workspace_root.join(top);
            if dir.exists() {
                collect_rs_files(&dir, &mut files);
            }
        }
        assert!(
            !files.is_empty(),
            "expected to find .rs source files under {}/{{apps,crates}} — \
             workspace root detection is broken",
            workspace_root.display()
        );

        let mut found = std::collections::BTreeSet::new();
        for file in &files {
            let content = std::fs::read_to_string(file).unwrap_or_default();
            for lit in extract_dot_ta_join_literals(&content) {
                found.insert(lit);
            }
        }

        let unaccounted: Vec<&String> = found
            .iter()
            .filter(|p| !is_known_or_allowlisted(p.trim_end_matches('/')))
            .collect();

        assert!(
            unaccounted.is_empty(),
            "Found .ta/ literal path(s) written by TA code that are not registered in \
             LOCAL_TA_PATHS or SHARED_TA_PATHS (crates/ta-workspace/src/partitioning.rs), \
             and not in this test's KNOWN_UNTRIAGED_GAPS allowlist: {:?}\n\
             Register each new path in LOCAL_TA_PATHS (if it's local runtime state) or \
             SHARED_TA_PATHS (if it should be committed).",
            unaccounted
        );
    }
