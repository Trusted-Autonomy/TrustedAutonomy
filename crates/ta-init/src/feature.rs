// feature.rs — Feature trait and built-in feature implementations for ta-init (v0.16.5).
//
// Each Feature is a small, focused struct that writes a specific subset of
// project files. Features are composable — a template declares which features
// it needs, and the engine applies them in order.

use std::{collections::HashMap, path::PathBuf};

// ── TemplateContext ──────────────────────────────────────────────────────────

/// Context passed to every Feature::apply() call.
pub struct TemplateContext {
    /// Absolute path to the project root.
    pub project_root: PathBuf,
    /// The human-readable project name (directory name or user-supplied).
    pub project_name: String,
    /// User-supplied and computed template variables.
    pub vars: HashMap<String, String>,
}

impl TemplateContext {
    pub fn ta_dir(&self) -> PathBuf {
        self.project_root.join(".ta")
    }
}

// ── Feature trait ────────────────────────────────────────────────────────────

/// A composable unit of project initialization logic.
///
/// Each implementation writes specific files/directories to the project tree.
/// Features are applied in the order declared by the template manifest.
pub trait Feature: Send + Sync {
    fn name(&self) -> &str;
    fn apply(&self, ctx: &TemplateContext) -> anyhow::Result<()>;
}

