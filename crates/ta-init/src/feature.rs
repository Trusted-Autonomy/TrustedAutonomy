// ── GitAdapterFeature ────────────────────────────────────────────────────────

/// Writes .gitattributes with project-memory merge strategy hint (if .git exists).
///
/// Mirrors generate_gitattributes_project_memory in init.rs.
pub struct GitAdapterFeature;

impl Feature for GitAdapterFeature {
    fn name(&self) -> &str {
        "git-adapter"
    }

    fn apply(&self, ctx: &TemplateContext) -> anyhow::Result<()> {
        if !ctx.project_root.join(".git").exists() {
            return Ok(());
        }
        write_gitattributes_project_memory(&ctx.project_root)
    }
}

fn write_gitattributes_project_memory(project_root: &std::path::Path) -> anyhow::Result<()> {
    let path = project_root.join(".gitattributes");
    let entry = ".ta/project-memory/*.json merge=union\n";

    if path.exists() {
        let content = std::fs::read_to_string(&path)?;
        if content.contains(".ta/project-memory/") {
            return Ok(()); // Already present.
        }
        let mut existing = content;
        if !existing.ends_with('\n') {
            existing.push('\n');
        }
        existing
            .push_str("# TA project-memory: use union merge to reduce conflicts (v0.15.13.3)\n");
        existing.push_str(entry);
        std::fs::write(&path, existing)?;
    } else {
        std::fs::write(
            &path,
            format!(
                "# .gitattributes — generated by `ta init`\n\
                 # TA project-memory: use union merge to reduce conflicts (v0.15.13.3)\n\
                 {}",
                entry
            ),
        )?;
    }
    println!("  Updated .gitattributes (project-memory merge strategy)");
    Ok(())
}

